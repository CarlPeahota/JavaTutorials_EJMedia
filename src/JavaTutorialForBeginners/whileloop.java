package JavaTutorialForBeginners;

public class whileloop {
    public static void main (String[] args) {
        
        int i = 1;
        int j = 15;

        while (i < j) {
            System.out.println (Integer.toString(i) + ") This is a loop");
            i++;
        }
            
        
        
    }
    
}
