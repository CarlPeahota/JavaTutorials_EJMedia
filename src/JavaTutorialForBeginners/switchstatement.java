package JavaTutorialForBeginners;

class switchstatement {

public static void main (String[] args) {
    int fieldgoal;
    fieldgoal = 32;
    
    switch (fieldgoal) {
    
    case 6: 
        System.out.println ("SCORE!!!");
        break;
    
    case 4343434: 
        System.out.println ("saftey");
        break;    
        
    case 3: 
        System.out.println ("field goal");
        break;        
    
    case 3000: 
        System.out.println ("NOT POSSIBLE SCORE");
        break;
        
    default:
        System.out.println ("NO MATCH");
        break;
    
}
}
}
    

